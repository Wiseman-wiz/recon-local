def create_sheet(sheetname, entries, type_of_list, company_code):
    current_row = 4
    aje = AJE_sheet(sheetname)
    for xentry in entries:
        entry_form = Entry_Formatter(xentry, type_of_list)
        entry, keys = entry_form.get_dict_and_keys(company_code)
        for key in keys:
            cell_code = entry_form.get_column_code(key, current_row)
            value = entry[key]
            aje.insert_data_on_cell(cell_code, value)
        current_row+=1
    aje.save()

def generate_aje_filename(type_of_list):
    aje_filename = rand_gen.get_random_filename(type_of_list)
    return aje_filename

